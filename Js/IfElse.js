/* controlador de fluxo
        -IF(se)
        -Else( se não)
    Operadores de comparação
        > Maior que
        < Menor que
        == Igual que
*/





const notaDoAluno = 7
const notaDeCorte = 5

/*Valor a ser comparado  < Valor determinado */
if (notaDoAluno < notaDeCorte){
    /*Oque será executado caso o if seja verdadeira */
    console.log("Parabens, voce foi aprovado")
}
else{
    /*Oque será executado caso o if seja falso */
    console.log("Infelizmente voce foi reprovado")
}
